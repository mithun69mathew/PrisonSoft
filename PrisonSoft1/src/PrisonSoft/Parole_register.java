private void fillcombo()
{
        try {
            ob.crearedatabase();
            String sql="select * from table_prisoner";
            ob.p=ob.con.prepareStatement(sql);
            ob.rs=ob.p.executeQuery();
            while(ob.rs.next())
            {
                String prisoner=ob.rs.getString("PRISONER_NAME");
                prcombo.addItem(prisoner);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Parole_register.class.getName()).log(Level.SEVERE, null, ex);
        }
}
