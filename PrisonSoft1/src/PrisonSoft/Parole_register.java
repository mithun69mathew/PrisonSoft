    private void prcomboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prcomboActionPerformed
        try {
            // TODO add your handling code here:
            ob.crearedatabase();
            String prisoner=prcombo.getSelectedItem().toString();
            String sql="select * from table_prisoner where prisoner_name='"+prisoner+"' ";
            ob.p=ob.con.prepareStatement(sql);
            ob.rs=ob.p.executeQuery();
            while(ob.rs.next())
            {
                String address=ob.rs.getString("address");
                int chest=ob.rs.getInt("CHESTNO");
                adcombo.addItem(address);
                chno.addItem(chest);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Parole_register.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_prcomboActionPerformed

