        // TODO add your handling code here:
        ob.crearedatabase();
         
        if(prcombo.getSelectedItem().toString().equals(null))
        {
             JOptionPane.showMessageDialog(null, "please select the prisoner name!!");
        }
        else if(visitorname.getText().equals(""))
         {
          JOptionPane.showMessageDialog(null, "please enter visitor name !!");
        }
        
        else if(visitoraddress.getText().equals("")) 
        {
         JOptionPane.showMessageDialog(null, "please enter visitor address!!");
        }
          else if(visittime.getText().equals("")) 
        {
         JOptionPane.showMessageDialog(null, "please enter visit time!!");
        }
          else if(visitdate.getDate().equals(""))
        {
         JOptionPane.showMessageDialog(null, "please convey visited date!!");
        }
          else if(visitpurpose.getText().equals(""))
        {
         JOptionPane.showMessageDialog(null, "please convey the purpose of visit!!");
        }
        else
          {
              try
             {
             String pname = prcombo.getSelectedItem().toString();
             String praddress = acombo.getSelectedItem().toString(); 
             String chest=chno.getSelectedItem().toString(); 
             int c=Integer.parseInt(chest);
             String vname = visitorname.getText();
             String vaddress = visitoraddress.getText();
             String vtime = visittime.getText();
             String vdate = (((JTextField)visitdate.getDateEditor().getUiComponent()).getText());
             String vreason = visitpurpose.getText();
             
             String sql="select prisoner_id from table_prisoner where PRISONER_NAME='"+pname+"' and ADDRESS='"+praddress+"' " ;
             ob.p=ob.con.prepareStatement(sql);
               ob.rs=ob.p.executeQuery();
                if(ob.rs.next())
                {
                    int id=ob.rs.getInt("prisoner_id");
                    System.out.print(id);
                    String sql1="insert into table_visitor values(DEFAULT,'"+pname+"','"+praddress+"','"+id+"','"+c+"','"+vname+"','"+vaddress+"','"+vtime+"','"+vdate+"','"+vreason+"','STATUS')";
                    ob.p=ob.con.prepareStatement(sql1);
                    int rs=ob.p.executeUpdate();
                    if(rs>0)
                    {
                       JOptionPane.showMessageDialog(null, "YOUR VISITOR REQUEST HAS BEEN FORWARDED!!");
                    }
                    else
                    {
                         JOptionPane.showMessageDialog(null, "SOMETHING WENT WRONG!!");
                    }
                }
                   
            }catch (SQLException ex) {
                Logger.getLogger(Parole_register.class.getName()).log(Level.SEVERE, null, ex);
            }
                
          }
        
