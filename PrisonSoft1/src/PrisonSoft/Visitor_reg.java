private void fetch()
{
        try {
            ob.crearedatabase();
            String sql="select * from table_prisoner";
            ob.p=ob.con.prepareStatement(sql);
            ob.rs=ob.p.executeQuery();
            while(ob.rs.next())
            {
                String prisonername=ob.rs.getString("PRISONER_NAME");
                System.out.print(prisonername);
               prcombo.addItem(prisonername);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Visitor_reg.class.getName()).log(Level.SEVERE, null, ex);
        }
}
