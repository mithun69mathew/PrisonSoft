    private void t1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t1KeyReleased
        try {
            // TODO add your handling code here:
            ob.crearedatabase();
            String search=t1.getText();
            DefaultTableModel tm=(DefaultTableModel)jTable5.getModel();
            TableRowSorter tr=new  TableRowSorter(tm);
            jTable5.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(search));
            String sql="select * from table_prisoner where PRISONER_NAME='"+search+"' ";
            ob.p=ob.con.prepareStatement(sql);
            ob.rs=ob.p.executeQuery();
            while(ob.rs.next())
            {
                String chestn=ob.rs.getString("CHESTNO");
                chno.addItem(chestn);
            }
        } catch (SQLException ex) {
             java.util.logging.Logger.getLogger(Prdet.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_t1KeyReleased

