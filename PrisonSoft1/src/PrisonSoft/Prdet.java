    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(t1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"PLEASE ENTER NECESSARY DETAILS OF PRISONERS");
        }
        else
        {
            try {
                ob.crearedatabase();
                String name=t1.getText();
                String chestno=chno.getSelectedItem().toString();
                int ch=Integer.parseInt(chestno);
                String sql="select * from table_prisoner where PRISONER_NAME='"+name+"' and CHESTNO='"+ch+"'";
                ob.p=ob.con.prepareStatement(sql);
                ob.rs=ob.p.executeQuery();
                if(ob.rs.next())
                {
                    String pimage=ob.rs.getString("PHOTO");
                    l2.setIcon(new ImageIcon(pimage));
                }
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(Prdet.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            String name=t1.getText();
            String chestno=chno.getSelectedItem().toString();
            int ch=Integer.parseInt(chestno);
            String dout=(((JTextField)dateofout.getDateEditor().getUiComponent()).getText());
            String sql="update table_prisoner set DATE_OF_OUT='"+dout+"' where PRISONER_NAME='"+name+"' and CHESTNO='"+ch+"' ";
            ob.p=ob.con.prepareStatement(sql);
            int rs=ob.p.executeUpdate();
            if(rs>0)
            {
                JOptionPane.showMessageDialog(null,name+" "+"CHESTNO"+" "+chestno+" " +"DATE OF OUT EXTENDED!!");
            }
        } catch (SQLException ex) {
             java.util.logging.Logger.getLogger(Prdet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

