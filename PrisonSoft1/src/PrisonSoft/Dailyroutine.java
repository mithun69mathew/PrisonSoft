private void fetch()
{
        try {
            ob.crearedatabase();
            String sql="select * from table_police where STATUS='guard' ";
            ob.p=ob.con.prepareStatement(sql);
            ob.rs=ob.p.executeQuery();
            while(ob.rs.next())
            {
                String guard=ob.rs.getString("POLICE_NAME");
                gcombo.addItem(guard);
            }
            String sql1="select * from table_prisoner";
            ob.p=ob.con.prepareStatement(sql1);
            ob.rs=ob.p.executeQuery();
            while(ob.rs.next())
            {
                String cellno=ob.rs.getString("CELLNO");
                cellcombo.addItem(cellno);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dailyroutine.class.getName()).log(Level.SEVERE, null, ex);
        }
       
}


