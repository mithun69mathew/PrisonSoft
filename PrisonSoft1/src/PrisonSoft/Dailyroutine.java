    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String guardname=gcombo.getSelectedItem().toString();
            String cellno=cellcombo.getSelectedItem().toString();
            int cno=Integer.parseInt(cellno);
            String work=wcombo.getSelectedItem().toString();
            String sql="select * from table_police where POLICE_NAME='"+guardname+"' ";
            ob.p=ob.con.prepareStatement(sql);
            ob.rs=ob.p.executeQuery();
            if(ob.rs.next())
            {
                int id=ob.rs.getInt("police_id");
                String sql1="insert into table_routine values(DEFAULT,'"+guardname+"','"+id+"','"+cno+"','"+work+"')";
                ob.p=ob.con.prepareStatement(sql1);
                int rs=ob.p.executeUpdate();
                if(rs>0)
                {
                    JOptionPane.showMessageDialog(null,"WORK ALLOTED");
                    gcombo.removeItem(guardname);
                    cellcombo.removeItem(cellno);
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(Dailyroutine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

